                                          CULTURE/LISSIMBOU       Honneur aux Femmes !
                                          Que ce soit dans la musique, ou lors des cérémonies matrimoniales, les femmes des communautés Mbahouin, Bayumbu, et Mindassa, magnifient la tradition LISSIMBOU.
                                          LISSIMBOU est un rite d’initiation réservé aux jeunes femmes, qui conduit de l’enfance à l’âge adulte, et qui a pour but entre autre, d’initier la jeune femme  à la responsabilité familiale et sociale. Comme pour la plupart des   rites traditionnels, la cérémonie publique fait suite à la première cérémonie tenue dans le secret de la brousse.  Jadis les femmes exécutaient les danses visibles à caractère mystique, par les seules initiées et les hommes circoncis.
                                          Les costumes traditionnels  caractéristiques du rite LISSIMBOU comprennent entre autres :
                                          La mise vestimentaire toute en raphia. Les matrones, les prêtresses et les fillettes portent différemment le raphia.
                                          La coiffure de Mbibanda (Tresses traditionnelles: les mèches ayant remplacés les chiffons d'antan)
                                          Les colliers de perles végétales traditionnelles Mayaka, portés, en bandoulière. Ces colliers perlés sont  utilisés comme marqueurs d’identité personnelle,  et culturelle. Les colliers posés en bandoulière, se croisent au milieu de la poitrine, une zone réceptrice , des états d’âme et des énergies.
                                          Les Plumes de Mbilè (L'aigle) et de Nkussu (perroquet)
                                          Mbilè, l'aigle est le symbole de la sagesse, de la force,et du pouvoir,  le perroquet est  le messager des Dieux et le symbole du voyage, notamment mystique.
                                          La peau d'Akoutou (Chat sauvage  en Mbahouin)  fortement symbolique de la connexion avec ce qui est habituellement caché ou le mystère de l’inconnu.
                                          Le chat sauvage est pour les Mbahouin, les Mindassa  et les Bayumbu le symbole de la lutte. Le chat sauvage est un animal lutteur, qui ne recule devant rien, il fait face à son adversaire, quelque soit sa taille et sa nature, à   l'image des Bayumbu,  Mindassa et Mbahouin qui  affrontent leurs adversaires sans peur.
                                          Tous ces symboles attribués aux différents éléments du costume traditionnel de l’initiée, accompagnent la future femme à   travers les différentes étapes de sa vie de mère et épouse.


